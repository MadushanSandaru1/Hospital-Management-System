                                <h2>අපේ කතාව</h2>
                                <hr>
                                <p class="mb-4 text-justify">ආරෝග්‍යා රෝහල      දශක හතකට වැඩි කාලයක් තිස්සේ ශ්‍රී ලංකා සෞඛ්‍ය සේවයේ විශ්වාසනීය නමකි. 
                                    1945 දී අපගේ පදනමේ සිට, සරල දර්ශනයක් මත පදනම්ව, වෛද්‍ය විශිෂ්ටත්වය හා නව්‍යකරණයන්හි කලාපීය නායකත්වය සඳහා කීර්තියක් අප විසින් ගොඩනගා ඇත: අපගේ ප්‍රජාවේ සෞඛ්‍යය නංවාලීම සඳහා ආශාව හා අනුකම්පාව දැක්විය යුතුය. ආරෝග්‍යා රෝහල ඇඳන් 260 ක් ලබා දෙයි - අපගේ විවේචනාත්මක සත්කාර දෙපාර්තමේන්තුවේ 60 ක් ඇතුළුව - ඉඩකඩ සහිත නවීන කාමර ගණනාවක් පුරා. අපි හොඳම උපදේශකයින්, විශේෂයින් සහ සේවකයින් පිරිනමන්නෙමු. ඔවුන් සියල්ලන්ම සුවිශේෂී සායනික ප්රතිපල සහ පාරිභෝගික තෘප්තිය ලබා දීමට කැපවී සිටිති. අති නවීන තාක්‍ෂණයෙන් අප ලෝක මට්ටමේ තෘතීයික සත්කාරක ආයතනයක් බවට පරිණාමය වී ඇත. අපගේ පහසුකම් අතර සිනමාහල් අටකින් සහ අති නවීන දැඩි සත්කාර ඒකක (ICU) වලින් සමන්විත අල්ට්‍රා මොඩර්න් රඟහල සංකීර්ණයක් ඇතුළත් වේ. නවීන විකිරණ විද්‍යා ඒකකයක්, අංග සම්පූර්ණ භෞත චිකිත්සක ඒකකයක් සහ කැතීටරීකරණ රසායනාගාර දෙකක් ද අපි පිරිනමන්නෙමු.
