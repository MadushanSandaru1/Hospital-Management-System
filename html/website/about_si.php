                                    ආරෝග්‍යා රෝහල කර්මාන්තයේ ප්‍රමුඛතම හෘද අණදෙන මධ්‍යස්ථානය සහ හෘද මධ්‍යස්ථානය ලබා දීමට ආඩම්බර වන අතර එයට රටේ හොඳම හෘද රෝග විශේෂයින් සහ හෘද ශල්‍ය වෛද්‍යවරුන්ගේ සහාය ලැබේ. ඇඳන් 15 ක් සහ ලෝක මට්ටමේ පහසුකම් සහිත මෙම මධ්‍යස්ථානය පුළුල් පරාසයක රෝග විනිශ්චය පරීක්ෂණ, උසස් ශල්‍ය ප්‍රතිකාර, හෘද පුනරුත්ථාපන සහ පශ්චාත් ශල්‍ය උපදේශන ලබා දෙයි. ආරෝග්‍යා රෝහල අපගේ දෙපාර්තමේන්තු පුරා කැපවූ හා දක්ෂ වෘත්තිකයන් දෙදහසකට අධික සංඛ්‍යාවක් සේවය කරන අතර ඔවුන් සියල්ලන්ම පාරිභෝගිකයින්ට සහ ඔවුන්ගේ පවුල්වලට සංවේදනයෙන් හා අනුකම්පාවෙන් සේවය කරයි. මෙම කණ්ඩායමට විශේෂ උපදේශකයින් 350 ක්, නේවාසික උපදේශකයින් 15 ක් සහ ජ්‍යෙෂ් වෛද්‍ය නිලධාරීන් 50 ක් පමණ ඉහළ පුහුණුව ලත් හෙදියන්, පරිපූරක වෛද්‍යවරුන් සහ වෛද්‍ය නොවන කාර්ය මණ්ඩලයේ සහාය ඇත. ගෝලීය සෞඛ්‍යාරක්ෂාව සඳහා රන් ප්‍රමිතිය සපුරාලීම සඳහා ඒකාබද්ධ කොමිෂන් ජාත්‍යන්තරය  විසින් අනුමත කරන ලද ශ්‍රී ලංකාවේ පළමු සෞඛ්‍ය සේවා ආයතනය ආරෝග්‍යා රෝහලයි. ඇමරිකා එක්සත් ජනපදය පදනම් කරගත් ලොව ප්‍රමුඛතම ප්‍රතීතන සංවිධානය වන JCI විසින් 2017 දී අපව අනුමත කිරීමේ රන් මුද්‍රාව සමඟ නැවත ප්‍රතීතනය කරන ලදී.</p>
